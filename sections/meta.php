<h1 class="title">Meta Tags</h1>
<h2 class="subtitle">As meta tags definem informações a respeito do documento,
    ou informações a respeito das informações a serem transmitidas,
    as meta tags de uma forma geral delimitam aspectos mais mais abstratos. <b>tags</b>:
    <mark>head, meta, base</mark>
</h2>


<details>
    <summary>head</summary>
    <p>O elemento HTML

        <head> providencia informações gerais (metadados) sobre o documento, incluindo seu título e links para scripts e folhas de estilos.
    </p>
</details>

<details>
    <summary>base</summary>
    <p>O elemento HTML base especifica o endereço (URL) utilizada por todos os endereços relativos contidos dentro de um documento. Há um número máximo de 1 (um) elemento Base
        <base> do documento.
    </p>
    <p>ex: se vc definir o base href='images' e colocar imagens dentro desta pasta,
        quando vc fizer um img src='imagen.png' a imagem será mostrada pois,
        automaticamente todos os links relativos serão inclusos no caminho images
    </p>
</details>